int main() {
	leds_init();
	// инициализируем диоды
	adc_init();
	// ацп
	adc_init2();
	
	init_USART1(9600);

	//ADC_TempSensorVrefintCmd(ENABLE);
	// разрешаем преобразование
	do {
		unsigned int Temperature = readADC1(10);
		//unsigned int value = readADC2(9);
		// читаем данные с датчика АЦП1
		if (Temperature > 1000) {
			// если больше 20 зажигаем первый диод
			GPIO_SetBits(GPIOD, GPIO_Pin_12);
		}

		if (Temperature > 2000) { // если больше 30 - второй. и т.д.
			GPIO_SetBits(GPIOD, GPIO_Pin_13);
		}

		if (Temperature > 3000) {
			GPIO_SetBits(GPIOD, GPIO_Pin_14);
		}

		if (Temperature > 4000) {
			GPIO_SetBits(GPIOD, GPIO_Pin_15);
		}
		USART_puts(USART1, Temperature);
		Delay(5000000);

		GPIO_ResetBits(GPIOD, GPIO_Pin_12 | GPIO_Pin_13 | GPIO_Pin_14 | GPIO_Pin_15);
		Delay(5000000);
		// задержка
	} while (1);
}